def main() -> int:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    for dirname in DIRECTORIES:
        (OUTPUT_DIR / dirname).mkdir(parents=True, exist_ok=True)

    content = OUTPUT_LAYOUT.rstrip() + "\n\n"
    content += f"Generated at: {datetime.now().isoformat(timespec='seconds')}\n"
    (OUTPUT_DIR / "OUTPUT_LAYOUT.md").write_text(content, encoding="utf-8")
    (OUTPUT_DIR / "code" / "README.md").write_text(CODE_README.rstrip() + "\n", encoding="utf-8")
    for relative_path, readme in SUBDIR_READMES.items():
        target = OUTPUT_DIR / relative_path
        target.parent.mkdir(parents=True, exist_ok=True)
        if not target.exists():
            target.write_text(readme.rstrip() + "\n", encoding="utf-8")
    print(f"✅ 已准备输出目录规划：{OUTPUT_DIR / 'OUTPUT_LAYOUT.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
